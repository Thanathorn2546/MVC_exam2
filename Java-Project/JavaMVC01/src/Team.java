
public class Team {
    private String teamName;
    private String teamScore;

    public String getTeamName() {
        return teamName;
    }

    public String setTeamName(String prm_team_name) {
        return this.teamName = prm_team_name;
    }

    public String getTeamScore() {
        return teamScore;
    }

    public String setTeamScore(String prm_team_score) {
        return this.teamScore = prm_team_score;
    }        
}
