public class Weather {
    private String weatDate;
    private String weatTemp;

    public String getWeatDate() {
        return weatDate;
    }

    public String setWeatDate(String prm_weat_date) {
        return this.weatDate = prm_weat_date;
    }

    public String getWeatTemp() {
        return weatTemp;
    }

    public String setWeatTemp(String prm_weat_temp) {
        return this.weatTemp = prm_weat_temp;
    }            
}
