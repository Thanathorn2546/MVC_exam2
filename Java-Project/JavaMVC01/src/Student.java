public class Student {
    private String studId;
    private String studScore;

    public String getStudId() {
        return studId;
    }

    public String setStudId(String prm_stud_id) {
        return this.studId = prm_stud_id;
    }

    public String getStudScore() {
        return studScore;
    }

    public String setStudScore(String prm_stud_score) {
        return this.studScore = prm_stud_score;
    }    
}
